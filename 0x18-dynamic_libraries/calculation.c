#include <stdio.h>
/**
 * sum - sum of 2 int
 * @x: 1 unit
 * @y: 2 unit
 * Return: sum
 */
int sum(int x, int y)
{
return (x + y);
}
/**
 * division - divv of 2 int
 * @x: 1 unit
 * @y: 2 unit
 * Return: division
 */
int division(int x, int y)
{
return (x / y);
}

/**
 * minus - min of 2 int
 * @x: 1 unit
 * @y: 2 unit
 * Return: min
 */
int minus(int x, int y)
{
return (x - y);
}

/**
 * times - mult of 2 int
 * @x: 1 unit
 * @y: 2 unit
 * Return:times
 */
int times(int x, int y)
{
return (x * y);
}

/**
 * modd - modd of 2 int
 * @x: 1 unit
 * @y: 2 unit
 * Return: mod
 */
int modd(int x, int y)
{
return (x % y);
}
